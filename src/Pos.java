public class Pos {

	public int x;
	public int y;
	
	public Pos(){
		this.x = 50;
		this.y = 65;
	}
	
	public Pos(int x, int y){
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	

}
